# All return Booleans

# EQUAL
equal = 5 == 6

# DIFFERENT
different = 5 != 6

# GREATER
greater = 5 > 6

# LESS
less = 5 < 6

# GREATER OR EQUAL
greater_or_equal = 5 >= 6

# LESS OR EQUAL
less_or_equal = 5 <= 6

print(equal)
print(different)
print(greater)
print(less)
print(greater_or_equal)
print(less_or_equal)
